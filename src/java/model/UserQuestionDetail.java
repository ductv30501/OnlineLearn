package model;

/**
 *
 * @author ADMIN
 */
public class UserQuestionDetail {

    private User user;
    private Quiz quiz;
    private Question question;
    private Option option;
    

    public UserQuestionDetail() {
    }

    public UserQuestionDetail(User user, Quiz quiz, Question question, Option option) {
        this.user = user;
        this.quiz = quiz;
        this.question = question;
        this.option = option;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Quiz getQuiz() {
        return quiz;
    }

    public void setQuiz(Quiz quiz) {
        this.quiz = quiz;
    }

    public Question getQuestion() {
        return question;
    }

    public void setQuestion(Question question) {
        this.question = question;
    }

    public Option getOption() {
        return option;
    }

    public void setOption(Option option) {
        this.option = option;
    }
    
    
}
